        <h3>国花杜康摘得香港明星慈善足球赛指定庆功用酒</h3>
        <p class="pcoal">
            Time: 2014-9-23&nbsp; &nbsp;
        </p>
        <ul>
            <p class="MsoNormal" style="text-align:left;text-indent:35pt;">
                <img class="lazy" src="/attached/image/20140923/20140923120705_9131.jpg" alt="">
            </p>
            <p class="MsoNormal" style="text-align:left;text-indent:35pt;">
                <img class="lazy" src="/attached/image/20140923/20140923120718_4134.jpg" alt="">
            </p>
            <p class="MsoNormal" style="text-align:left;text-indent:35pt;">
                <img class="lazy" src="/attached/image/20140923/20140923120732_3667.jpg" alt="">
            </p>
            <p class="MsoNormal" style="text-align:left;text-indent:35pt;">
                <img class="lazy" src="/attached/image/20140923/20140923120741_0699.jpg" alt="">
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                <span style="line-height:1.5;">谁说香港明星只爱喝洋酒，而对中国的白酒不感兴趣？</span><span style="line-height:1.5;">9</span><span style="line-height:1.5;">月</span><span style="line-height:1.5;">20</span><span style="line-height:1.5;">日，在郑州举行的香港明星慈善足球赛慈善晚宴上，作为此次大赛的指定庆功白酒，国花杜康</span><span style="line-height:1.5;">15</span><span style="line-height:1.5;">年就受到罗家英等多位港星的追捧。</span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                事实上，国花杜康不仅在晚宴上引人注目，即使在众多爱心力量汇聚的球赛现场，凭借请来“蓉儿”现场为“靖哥哥”黄日华加油助威的独特创意，也成功吸引了上万名观众的目光，成为赛场之外最抢眼的亮点。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                以至于多位河南酒类行业营销专家感慨说，虽然不是香港明星慈善足球赛的主要赞助商，但国花杜康却巧妙地借势营销，既提升了产品销量，也提升了品牌活跃度和美誉度，取得了“四两拨千斤”的良好效果，值得其他酒类企业借鉴。
            </p>
            <p class="MsoNormal" style="text-indent:35.15pt;">
                <b>巧妙借势<span></span></b>
            </p>
            <p class="MsoNormal" style="text-indent:35.15pt;">
                <b>杜康请“蓉妹妹”助阵“靖哥哥”</b>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                如果你到了香港明星慈善足球赛的现场，想必对这个场景记忆犹新： 在赛场主席台的正后上方，“蓉儿”率领亲友团为“靖哥哥”加油助阵。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                难道真的是“蓉儿”穿越时光了？当然不是，这是洛阳杜康控股有限公司和龙凤宫华夏古装摄影城的跨界营销创意。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                “得知‘靖哥哥’黄日华（饰演<span>1983</span>版电视剧《射雕英雄传》中的郭靖），将与罗家英、李子雄等人到郑州开踢香港明星慈善足球赛，我们就想到了全城海选‘蓉儿’到球赛现场加油，以表达对这些港星支持河南贫困地区儿童教育的感谢之情。”洛阳杜康控股销售公司总经理助理徐振江介绍说。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:42.0pt;">
                这个想法，很快依托洛阳杜康控股有限公司和龙凤宫华夏古装摄影城的微信公众平台实施，短短一天时间，文章总阅读量高达数万人次，而参选的女孩也多达<span>100</span>多位，最终郑州籍姑娘韩三以清秀的相貌、古典的气质当选“蓉儿”。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:42.0pt;">
                “<span>9</span>月<span>20</span>日下午，我们将‘蓉儿’打扮成一袭白衣的古代侠女，手举‘靖哥哥，蓉儿在郑州挺你’的宣传牌，率领国花杜康·龙凤宫亲友团几十人赶赴球赛现场，为黄日华等香港明星球员加油助威，成功吸引了上万名观众的目光。”
                洛阳杜康控股销售公司总经理助理徐振江透露，当天‘蓉儿’的人气仅次于香港明星，前来与她合影留念的观众络绎不绝，成为赛场之外最抢眼的亮点。
            </p>
            <p class="MsoNormal" style="text-indent:35.15pt;">
                <b>助兴晚宴<span></span></b>
            </p>
            <p class="MsoNormal" style="text-indent:35.15pt;">
                <b>国花杜康备受罗家英等港星追捧</b>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                事实上，国花杜康不仅在赛场上引人注目，即使在当晚举行的香港明星慈善足球赛慈善晚宴上，它也表现得格外抢眼。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                据了解，此次香港明星慈善足球赛，共有数十位香港明星参与，其中不乏黄日华、罗家英、李子雄等知名人物，而且不为商业，只为声援河南贫困地区儿童教育事业，因此成为河南酒类企业的争抢焦点。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                但综合历史文化底蕴、品牌知名度、产品品质等多种因素，最终国花杜康<span>15</span>年摘得香港明星慈善足球赛慈善晚宴指定庆功白酒，成为助兴宴会的河南酒类行业中唯一酒品。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                作为洛阳杜康控股有限公司的核心产品之一，国花杜康<span>15</span>年在慈善晚宴上自然不负众望。它凭借好看的外观，馥郁的酒香，引得多位习惯了喝洋酒的香港明星品尝，而曾经饰演电影《大话西游》中“唐僧“的罗家英，不仅在现场询问国花杜康的情况，更是明确表示要将它带回香港与亲友品尝。<span></span>
            </p>
            <p class="MsoNormal" style="text-indent:35.0pt;">
                国花杜康真被如此热捧？如果你觉得杜康的说法有王婆卖瓜之嫌，或许当晚<span>12</span>点多，一位参加慈善晚宴的企业高管发给国花杜康郑州市区经理刘志锋的短信，最能说明真相：“国花杜康，牛！我发现五六位香港明星，晚宴结束后，都手拿着国花杜康<span>15</span>年走了，不知道是想收藏做纪念，还是准备睡觉前再喝两口？“<span></span>
            </p>
